public class Primitivos {
    public static void main(String[] args){
        // tipos primitivos

        boolean isLogged = true; // Possibilita condições
        byte b = 127; // Usado em criptografia, manipular caracteres e manipular imagens
        byte by = 'a'; // pode receber bytes de um caractere
        char c = 'c'; // recebe uma única letra/caractere
        short s = -129; // = int (só com menos espaços)
        int i = 123123321; // grande em relação ao short
        long l = 1231283912839821L; // = int (só que com mais espaços e necessita "L" no final)
        float f = 19.19f; // 32bit (fracionados, menor do que double e necessita "f" no final)
        double d = 19.19; // 64bit (fracionados, maior número possível)

        // não primitivo
        String character = "c"; // recebe uma cadeia de caracteres

    }
}
